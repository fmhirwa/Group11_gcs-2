#!/bin/bash

# Print strings on separate lines
echo "one"
echo "two"
echo "three"
echo "four"
echo "five"

# Create text file with today's date
filename="$(date +"%Y-%m-%d")-file.txt"
touch "$filename"
echo "one" >> "$filename"
echo "two" >> "$filename"
echo "three" >> "$filename"
echo "four" >> "$filename"
echo "five" >> "$filename"
